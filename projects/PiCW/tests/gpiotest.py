import RPi.GPIO as GPIO

#
# Pin assign for RPi1 B rev.2
#
#        corner of the board
#                         /
#                  +-----+
#             3.3V |01|02| 5V
#  I2C+ SDA GPIO02 |03|04| 5V
#     + SCL GPIO03 |05|06| GND
#           GPIO04 |07|08| GPIO14 TXD +UART
#              GND |09|10| GPIO15 RXD +
#  UART-RTS GPIO17 |11|12| GPIO18 PWM
#           GPIO27 |13|14| GND
#           GPIO22 |15|16| GPIO23
#             3.3V |17|18| GPIO24
# SPI+ MOSI GPIO10 |19|20| GND
#    + MISO GPIO09 |21|22| GPIO25
#    +  CLK GPIO11 |23|24| GPIO08 CE0 +SPI
#              GND |25|26| GPIO07 CE1 +
#                  +-----+
#
# P1 Header pinout, top row
# +-------------------------------------------------------------+
# |Pin   |Pin Name Rev1|Hardware   |Alt 0     |Other Alternative|
# |Number|         Rev2|Notes      |Function  |Functions        |
# |------+-------------+-----------+----------+-----------------|
# |P1-02 |5V0          |Supply     |          |                 |
# |      |             |through    |          |                 |
# |------+-------------+input      +----------+-----------------|
# |P1-04 |5V0          |poly       |          |                 |
# |      |             |fuse       |          |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-06 |GND          |           |          |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-08 |GPIO 14      |Boot       |UART0_TXD |ALT5 = UART1_TXD |
# |------+-------------+to         +----------+-----------------|
# |P1-10 |GPIO 15      |Alt 0 ->   |UART0_RXD |ALT5 = UART1_RXD |
# |------+-------------+-----------+----------+-----------------|
# |P1-12 |GPIO 18      |           |PCM_CLK   |ALT4 = SPI1_CE0_N|
# |      |             |           |          |ALT5 = PWM0      |
# |------+-------------+-----------+----------+-----------------|
# |P1-14 |GND          |           |          |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-16 |GPIO23       |           |          |ALT3 = SD1_CMD   |
# |      |             |           |          |ALT4 = ARM_RTCK  |
# |------+-------------+-----------+----------+-----------------|
# |P1-18 |GPIO24       |           |          |ALT3 = SD1_DAT0  |
# |      |             |           |          |ALT4 = ARM_TDO   |
# |------+-------------+-----------+----------+-----------------|
# |P1-20 |GND          |           |          |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-22 |GPIO25       |           |          |ALT3 = SD1_DAT1  |
# |      |             |           |          |ALT4 = ARM_TCK   |
# |------+-------------+-----------+----------+-----------------|
# |P1-24 |GPIO08       |           |SPI0_CE0_N|                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-26 |GPIO07       |           |SPI0_CE1_N|                 |
# +-------------------------------------------------------------+
# P1 Header pinout, bottom row
# +-------------------------------------------------------------+
# |Pin   |Pin Name     |Hardware   |Alt 0     |Other Alternative|
# |Number|Rev1  |Rev2  |Notes      |Function  |Functions        |
# |------+-------------+-----------+----------+-----------------|
# |P1-01 |3.3 V        |50 mA max  |          |                 |
# |      |             |(01 & 17)  |          |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-03 |GPIO 0|GPIO 2|1K8 pull up|I2C0_SDA /|                 |
# |      |      |      |resistor   |I2C1_SDA  |                 |
# |------+------+------+-----------+----------+-----------------|
# |P1-05 |GPIO 1|GPIO 3|1K8 pull up|I2C0_SCL /|                 |
# |      |      |      |resistor   |I2C1_SCL  |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-07 |GPIO 4       |           |GPCLK0    |ALT5 = ARM_TDI   |
# |------+-------------+-----------+----------+-----------------|
# |P1-09 |GND          |           |          |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-11 |GPIO17       |           |          |ALT3 = UART0_RTS |
# |      |             |           |          |ALT4 = SPI1_CE1_N|
# |      |             |           |          |ALT5 = UART1_RTS |
# |------+-------------+-----------+----------+-----------------|
# |P1-13 |GPIO21|GPIO27|           |PCM_DOUT  |ALT4 = SPI1_SCLK |
# |      |      |      |           |          |ALT5 = GPCLK1    |
# |      |      |      |           |/ reserved|/ALT3 = SD1_DAT3 |
# |      |      |      |           |          | ALT4 = ARM_TMS  |
# |------+-------------+-----------+----------+-----------------|
# |P1-15 |GPIO22       |           |          |ALT3 = SD1_CLK   |
# |      |             |           |          |ALT4 = ARM_TRST  |
# |------+-------------+-----------+----------+-----------------|
# |P1-17 |3.3 V        |50 mA max  |          |                 |
# |      |             |(01 & 17)  |          |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-19 |GPIO10       |           |SPI0_MOSI |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-21 |GPIO9        |           |SPI0_MISO |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-23 |GPIO11       |           |SPI0_SCLK |                 |
# |------+-------------+-----------+----------+-----------------|
# |P1-25 |GND          |           |          |                 |
# +-------------------------------------------------------------+
#
# (refering to https://elinux.org/RPi_Low-level_peripherals#General_Purpose_Input.2FOutput_.28GPIO.29)
#
# Usage example  -  http://ag.hatenablog.com/entry/2015/07/31/013013

GPIO.setmode(GPIO.BCM)
GPIO.cleanup()
